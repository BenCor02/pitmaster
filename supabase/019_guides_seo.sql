 '## Le stall : le cauchemar du débutant

Tu surveilles ton brisket depuis 4 heures. La température interne monte régulièrement : 40°C, 50°C, 60°C... et soudain, **ça s''arrête**. La sonde reste bloquée à 68°C pendant 2, 3, parfois **4 heures**. Bienvenue dans le **stall** (le plateau).

### Pourquoi ça arrive ?

Le stall est causé par l''**évaporation de l''eau** à la surface de la viande. C''est exactement le même phénomène que la transpiration qui refroidit ton corps.

Quand la viande atteint ~65°C, l''humidité interne commence à migrer vers la surface et s''évapore. Cette évaporation **refroidit la viande** exactement autant que le fumoir la chauffe. Résultat : la température stagne.

### Combien de temps ça dure ?

- **Brisket** : 2 à 5 heures (le pire)
- **Épaule de porc** : 1 à 3 heures
- **Ribs** : stall léger ou inexistant

### Comment gérer le stall ?

#### Option 1 : La patience (puriste)
Ne fais rien. Attends. Le stall finit toujours par passer quand toute l''humidité de surface s''est évaporée. C''est la méthode des puristes texans.

**Avantage** : bark maximale, saveur pure
**Inconvénient** : cuisson beaucoup plus longue (14-18h pour un brisket)

#### Option 2 : Le wrap — « Texas Crutch »
Enveloppe la viande dans du papier alu ou du papier boucher rose quand la température interne atteint 68-74°C.

- **Papier alu** : arrête l''évaporation complètement. La viande traverse le stall en 1h. Mais la bark ramollit.
- **Papier boucher rose** (peach/butcher paper) : laisse passer un peu de vapeur. Compromis : stall raccourci ET bark préservée. **C''est le choix recommandé.**

#### Option 3 : Monter la température
Passe de 107°C à 135°C au fumoir. La chaleur supplémentaire dépasse le refroidissement par évaporation. Mais attention : le risque de sécher la viande augmente.

### Le stall et le collagène

Bonne nouvelle : le stall correspond **exactement** à la plage de température où le collagène se transforme en gélatine (65-80°C). Donc pendant que tu attends, la magie opère : les tissus conjonctifs fondent et la viande devient tendre.

**Ne coupe pas ta cuisson pendant le stall.** C''est le moment le plus important.

