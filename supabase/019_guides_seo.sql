La poitrine entière se compose de deux muscles :

- **Le flat** (ou « plat ») : la partie fine et maigre. C''est celle qu''on tranche. Elle a tendance à sécher si mal cuite.
- **Le point** (ou « pointe ») : la partie épaisse et grasse. C''est la plus savoureuse, souvent utilisée pour les burnt ends (cubes de poitrine caramélisés).

